#include "ft_printf.h"

int	ft_print_str(char *s)
{
	int	i;

	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = -1;
	while (s[++i] != '\0')
		write(1, &s[i], 1);
	return (i);
}
